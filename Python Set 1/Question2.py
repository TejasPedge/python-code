# 2. **Data Type Play**: Create variables of each data type (integer, float, string, boolean, list, tuple, dictionary, set) and print their types and values.
#     - *Input*: None
#     - *Output*: "Type of variable1: <class 'int'>, value: 10..."


num_int = 10
print("Type of num_int:", type(num_int), "Value:", num_int)


num_float = 3.14
print("Type of num_float:", type(num_float), "Value:", num_float)


string = "Hello, World!"
print("Type of string:", type(string), "Value:", string)


bool_var = True
print("Type of bool_var:", type(bool_var), "Value:", bool_var)


my_list = [1, 2, 3]
print("Type of my_list:", type(my_list), "Value:", my_list)


my_tuple = (4, 5, 6)

print("Type of my_tuple:", type(my_tuple), "Value:", my_tuple)

my_dict = {'a': 7, 'b': 8, 'c': 9}
print("Type of my_dict:", type(my_dict), "Value:", my_dict)


my_set = {10, 11, 12}
print("Type of my_set:", type(my_set), "Value:", my_set)
